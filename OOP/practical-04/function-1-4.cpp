#include<iostream>
#include<string>
using namespace std;

void cpyda(double *old_array,double *new_array,int length){
    double pointer[3][2]={};
    for(int i=0; i<length;i++){
        for(int j=0;j<2;j++){
            pointer[i][j]=*old_array;
            *new_array=pointer[i][j];
        }
    }
    
}
