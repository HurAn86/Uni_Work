int sumtwo(int array[], int secondarray[], int n){
    int total1 = 0;
    int total2 = 0;
    int total =0;
    for(int i =0; i < n; i++){
        if(n < 1){
            total = 0;
        }else{
            total1 += array[i];
            total2 += secondarray[i];
        }
    }
    total = total1 + total2;
    
    return total;
}

