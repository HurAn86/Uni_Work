#include <iostream>
#include <string>
#include<math.h>
#include<vector>
using namespace std;
int binary_to_number(int binary_digits[], int number_of_digits){
    int sum = 0;
    for(int i = 0; i < number_of_digits; i++){
        if(binary_digits[i] == 1){
            sum += pow(2,i);
        }
    }
    return sum;
}
